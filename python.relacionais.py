valorA = 1
valorB = 2


if valorA > valorB:
    from tkinter import messagebox
    messagebox.showinfo('Relacionais' , 'maior')
    print('maior')

if valorA >= valorB:
    from tkinter import messagebox
    messagebox.showinfo('Relacionais' , 'maior ou igual')
    print('maior ou igual')

if valorA < valorB:
    from tkinter import messagebox
    messagebox.showinfo('Relacionais' , 'menor')
    print('menor')

if valorA <= valorB:
    from tkinter import messagebox
    messagebox.showinfo('Relacionais', 'menor ou igual')
    print('menor ou igual')
